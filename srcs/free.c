#include <hotrace.h>

static void		ft_free_tree(t_tree *t)
{
	if (!t)
		return ;
	ft_free_tree(t->left);
	ft_free_tree(t->right);
	free(t);
}

int				ft_free(t_a *a)
{
	int		i;

	free(a->str);
	i = -1;
	while (++i < SIZE_ARR)
		ft_free_tree(a->arr[i]);
	return (SUCCESS);
}
