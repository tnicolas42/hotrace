#include <hotrace.h>

int	ft_len(char *s, char c)
{
	unsigned	x;
	int			len;

	len = 0;
	while (42)
	{
		x = *(unsigned*)s;
		if ((x & 0xFF) == 0 || (x & 0xFF) == c)
			return (len);
		if ((x & 0xFF00) == 0 || (x & 0xFF00) >> 8 == c)
			return (len + 1);
		if ((x & 0xFF0000) == 0 || (x & 0xFF0000) >> 16 == c)
			return (len + 2);
		if ((x & 0xFF000000) == 0 || (x & 0xFF000000) >> 24 == c)
			return (len + 3);
		s += 4;
		len += 4;
	}
}
